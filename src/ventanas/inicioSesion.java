        contraseña = pass;
        if (sistema.personaCorrecta(nombre, contraseña,modo)) {
            if (modo==0){
            Usuario u= sistema.devolucionUsuario(nombre);
            Menu menu = new Menu(u, sistema);
            menu.setVisible(true);
            this.setVisible(false);}
        } else {
            error();
        }

